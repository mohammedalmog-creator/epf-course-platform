// MAIN SEED FUNCTION
// ============================================================
async function seedWellheadCourse() {
  const conn = await mysql.createConnection(process.env.DATABASE_URL);
  
  try {
    console.log('🚀 Starting Wellhead Maintenance course seed...');
    
    // Check if course already exists
    const [existing] = await conn.execute(
      'SELECT id FROM courses WHERE slug = ?',
      ['wellhead-maintenance']
    );
    
    if (existing.length > 0) {
      console.log('⚠️  Wellhead course already exists. Deleting and re-seeding...');
      const courseId = existing[0].id;
      await conn.execute('DELETE FROM quiz_questions WHERE course_id = ?', [courseId]);
      await conn.execute('DELETE FROM lessons WHERE course_id = ?', [courseId]);
      await conn.execute('DELETE FROM modules WHERE course_id = ?', [courseId]);
      await conn.execute('DELETE FROM courses WHERE id = ?', [courseId]);
    }
    
    // Insert course
    const [courseResult] = await conn.execute(
      `INSERT INTO courses (title_ar, title_en, description_ar, description_en, slug, level, duration_hours, image_url, is_active)
       VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?)`,
      [
        'صيانة رأس البئر — البري والبحري',
        'Wellhead Maintenance — Onshore & Offshore',
        'دورة شاملة في صيانة رأس البئر تغطي المكونات والأنظمة والإجراءات والسلامة للبيئتين البرية والبحرية',
        'A comprehensive course in wellhead maintenance covering components, systems, procedures, and safety for both onshore and offshore environments',
        'wellhead-maintenance',
        'intermediate',
        40,
        'https://images.unsplash.com/photo-1518709268805-4e9042af9f23?w=800',
        true
      ]
    );
    const courseId = courseResult.insertId;
    console.log(`✅ Course created with ID: ${courseId}`);
    
    // Group lessons by module
    const moduleMap = {};
    for (const lesson of lessons) {
      if (!moduleMap[lesson.moduleNumber]) {
        moduleMap[lesson.moduleNumber] = [];
      }
      moduleMap[lesson.moduleNumber].push(lesson);
    }
    
    // Insert modules and lessons
    const moduleIdMap = {};
    for (const mod of modules) {
      const [modResult] = await conn.execute(
        `INSERT INTO modules (course_id, module_number, title_ar, title_en, description_ar, description_en, order_index)
         VALUES (?, ?, ?, ?, ?, ?, ?)`,
        [courseId, mod.moduleNumber, mod.titleAr, mod.titleEn, mod.descriptionAr, mod.descriptionEn, mod.moduleNumber]
      );
      moduleIdMap[mod.moduleNumber] = modResult.insertId;
      console.log(`  ✅ Module ${mod.moduleNumber}: ${mod.titleEn}`);
      
      // Insert lessons for this module
      const moduleLessons = moduleMap[mod.moduleNumber] || [];
      for (const lesson of moduleLessons) {
        await conn.execute(
          `INSERT INTO lessons (course_id, module_id, title_ar, title_en, content, order_index, estimated_minutes)
           VALUES (?, ?, ?, ?, ?, ?, ?)`,
          [courseId, modResult.insertId, lesson.titleAr, lesson.titleEn, lesson.content, lesson.order, lesson.estimatedMinutes]
        );
      }
      console.log(`    ✅ ${moduleLessons.length} lessons inserted for module ${mod.moduleNumber}`);
    }
    
    // Insert quiz questions
    let questionCount = 0;
    for (const q of quizQuestions) {
      const moduleId = moduleIdMap[q.moduleNumber];
      if (!moduleId) {
        console.warn(`⚠️  No module found for question in module ${q.moduleNumber}`);
        continue;
      }
      await conn.execute(
        `INSERT INTO quiz_questions (course_id, module_id, question_text_ar, question_text_en, options_json, correct_option_id, explanation_ar, explanation_en)
         VALUES (?, ?, ?, ?, ?, ?, ?, ?)`,
        [
          courseId,
          moduleId,
          q.questionTextAr,
          q.questionTextEn,
          JSON.stringify(q.optionsJson),
          q.correctOptionId,
          q.explanationAr || '',
          q.explanationEn || ''
        ]
      );
      questionCount++;
    }
    console.log(`✅ ${questionCount} quiz questions inserted`);
    
    console.log('\n🎉 Wellhead Maintenance course seeded successfully!');
    console.log(`   Course ID: ${courseId}`);
    console.log(`   Modules: ${modules.length}`);
    console.log(`   Lessons: ${lessons.length}`);
    console.log(`   Quiz Questions: ${questionCount}`);
    
  } finally {
    await conn.end();
  }
}

seedWellheadCourse().catch(console.error);
